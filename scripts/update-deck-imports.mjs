// Script to automatically generate deck imports for index.astro
import fs from 'node:fs';
import path from 'node:path';

const dataDir = path.resolve('src/data');
const indexAstroPath = path.resolve('src/pages/index.astro');

console.log('Scanning for deck files...');

// Scan for deck files
const deckFiles = fs.existsSync(dataDir)
  ? fs.readdirSync(dataDir).filter(f => f.endsWith('.js')).sort()
  : [];

if (deckFiles.length === 0) {
  console.error('No deck files found in src/data/');
  process.exit(1);
}

console.log(`Found ${deckFiles.length} deck files:`, deckFiles);

// Read each generated JS file to discover its export name
// (matches `export const <name> = [`)
const getExportName = (filePath) => {
  const content = fs.readFileSync(filePath, 'utf-8');
  const match = content.match(/export const (\w+)\s*=/);
  return match ? match[1] : null;
};

// Generate imports and config
const imports = [];
const configs = [];

for (const file of deckFiles) {
  const deckId = path.basename(file, '.js');
  const filePath = path.join(dataDir, file);
  const exportName = getExportName(filePath);

  if (!exportName) {
    console.warn(`Could not find export in ${file}, skipping`);
    continue;
  }

  // Display name: "foot-ankle" → "Foot/Ankle", "shoulder" → "Shoulder"
  const displayName = deckId
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join('/');

  imports.push(`import { ${exportName} } from '../data/${file}';`);
  configs.push(`  { id: '${deckId}', module: ${exportName}, name: '${displayName}' }`);
}

// Read current index.astro file
let content = fs.readFileSync(indexAstroPath, 'utf-8');

// Build replacement blocks
const newImportsBlock = `// Import all available decks directly (auto-generated by update-deck-imports.mjs)\n${imports.join('\n')}`;
const newConfigsBlock = `// Available decks configuration (auto-generated by update-deck-imports.mjs)\nconst deckConfigs = [\n${configs.join(',\n')}\n];`;

// Replace the imports section: match from the comment through ALL consecutive import lines
content = content.replace(
  /\/\/ Import all available decks[^\n]*\n(?:import [^\n]*\n)*/,
  newImportsBlock + '\n'
);

// Replace the deck configs section: match from the comment through the closing ];
content = content.replace(
  /\/\/ Available decks configuration[^\n]*\nconst deckConfigs = \[[\s\S]*?\];/,
  newConfigsBlock
);

// Write the updated file
fs.writeFileSync(indexAstroPath, content, 'utf-8');

console.log('Updated index.astro with deck imports and configurations');
console.log(`Configured ${deckFiles.length} decks:`, configs.map(c => c.match(/name: '([^']+)'/)[1]));
