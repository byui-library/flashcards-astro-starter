// Script to automatically generate deck imports for index.astro
import fs from 'node:fs';
import path from 'node:path';

const dataDir = path.resolve('src/data');
const indexAstroPath = path.resolve('src/pages/index.astro');

console.log('🔍 Scanning for deck files...');

// Scan for deck files
const deckFiles = fs.existsSync(dataDir) 
  ? fs.readdirSync(dataDir).filter(f => f.endsWith('.js'))
  : [];

if (deckFiles.length === 0) {
  console.log('❌ No deck files found in src/data/');
  process.exit(1);
}

console.log(`📦 Found ${deckFiles.length} deck files:`, deckFiles);

// Generate imports and config
const imports = [];
const configs = [];

for (const file of deckFiles) {
  const deckId = path.basename(file, '.js');
  const camelCaseName = deckId.replace(/[-_]([a-z])/g, (g) => g[1].toUpperCase());
  const displayName = deckId
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(': ');

  imports.push(`import { ${camelCaseName} } from '../data/${file}';`);
  configs.push(`  { id: '${deckId}', module: ${camelCaseName}, name: '${displayName}' }`);
}

// Read current index.astro file
let content = fs.readFileSync(indexAstroPath, 'utf-8');

// Replace the imports section
const importsSection = imports.join('\n');
const newImportsBlock = `// Import all available decks directly (auto-generated by update-deck-imports.mjs)
${importsSection}`;

// Replace the deck configs section
const configsSection = configs.join(',\n');
const newConfigsBlock = `// Available decks configuration (auto-generated by update-deck-imports.mjs)
const deckConfigs = [
${configsSection}
];`;

// Update the content
content = content.replace(
  /\/\/ Import all available decks.*?\n.*?import.*?;/s,
  newImportsBlock
);

content = content.replace(
  /\/\/ Available decks configuration.*?\n.*?const deckConfigs = \[[\s\S]*?\];/,
  newConfigsBlock
);

// Write the updated file
fs.writeFileSync(indexAstroPath, content, 'utf-8');

console.log('✅ Updated index.astro with deck imports and configurations');
console.log(`📊 Configured ${deckFiles.length} decks:`, configs.map(c => c.match(/name: '([^']+)'/)[1]));